					<p>SEO can be divided into 3 main processes : </p>
					<ul>
						<li>Technical - The work done by the techie so that the site performs well with search engines.</li>
						<li>Content - The work done to maximise key phrases picked up by the search engines, but also make the words meaningful to the user.</li>
						<li>Link building - The process of getting content from other (preferably high quality) websites pointing at yours.</li>
					</ul>
